import Foundation

enum AppLanguage: String {
    case ru
    case en
    case zh

    static var current: AppLanguage {
        let code = (Locale.preferredLanguages.first ?? "en").lowercased()
        if code.hasPrefix("ru") {
            return .ru
        }
        if code.hasPrefix("zh") {
            return .zh
        }
        return .en
    }

    var workerCode: String {
        rawValue
    }
}

