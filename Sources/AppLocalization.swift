    static func t(_ key: String) -> String {
        strings[language]?[key] ?? strings[.en]?[key] ?? key
    }

    static func f(_ key: String, _ args: CVarArg...) -> String {
        String(format: t(key), locale: Locale.current, arguments: args)
    }

